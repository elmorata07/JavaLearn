    public void mostrarDatos() {
        //el modelo va a quedar con la cantidad de filas de la historia del paciente
//        if (this.m != null && this.m.getHistorials() != null) {
            modelo.setNumRows(this.m.getHistorials().size());
            //recorre el arraylist de mascota hasta terminar
            for (int i = 0; i < this.m.getHistorials().size(); i++) {
                //setee el valor en 
                modelo.setValueAt(this.m.getHistorials().get(i).getCodigo(), i, 0);
                modelo.setValueAt(this.m.getHistorials().get(i).getDescripcion(), i, 1);
//                if (this.m.getHistorials().get(i).getProcedimientos()!=null) {
                    modelo.setValueAt(this.m.getHistorials().get(i).getProcedimientos().getNombre(), i, 2);
//                } else {
                    modelo.setValueAt("N/A", i, 2);
//                }
                
            }
            //setea el modelo
            jTDatos.setModel(modelo);
//        } else{
//            System.out.println("Error: rl objeto 'm'o su historial es null.");
//        }

    }

