    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (jTDatos.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Selecciona el numero a eliminar");
        } else {
            this.m.getHistorials().remove(jTDatos.getSelectedRow());
            this.mostrarDatos();
        }
  
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        Historial n = new Historial();
        n.setCodigo(codigo);
        codigo++;
        n.setDescripcion(jTextDescripcion.getText());
        //limpiamos texto
        jTextDescripcion.setText("");
        n.setProcedimientos(this.procedimientos.get(jCProcedimiento.getSelectedIndex() - 1));
        this.m.getHistorials().add(n);
        //agrega el procedimiento a historial
        this.mostrarDatos();
    }//GEN-LAST:event_btnGuardarActionPerformed

